    private void btnSairDaFilaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSairDaFilaActionPerformed
        String matricula = usuario.getMatricula();
        int linhas = jTableUsuarios.getRowCount();
        boolean tem = this.tem_na_lista(linhas, matricula);
        
        if(tem)
        {
            fila.remover(matricula);        
            this.preencherTabela();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Usuario não registrado!");
        }
        
    }//GEN-LAST:event_btnSairDaFilaActionPerformed
    
    private Boolean tem_na_lista(int linhas, String matricula)
    {
        for(int i = 0; i < linhas; i++)
        {
            String item = (String) jTableUsuarios.getValueAt(i, 0);
            if(item.equals(matricula))
            {
                return true;
            }
        }
        
        return false;
    }
    
