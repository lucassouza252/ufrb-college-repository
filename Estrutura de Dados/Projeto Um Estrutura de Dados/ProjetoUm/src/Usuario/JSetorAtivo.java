    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String matricula = usuario.getMatricula();
        int linhas = jTableUsuarios.getRowCount();
        
        usuario.setChamada(this.sortear_chamada());
        
        boolean tem = this.tem_na_lista(linhas, matricula);
        if(tem)
        {
            JOptionPane.showMessageDialog(null, "Usuario já registrado!");
        }
        else
        {
            try {
                dados_arquivo.salvar(usuario, nome_setor);
            } catch (IOException ex) {
                Logger.getLogger(JSetorAtivo.class.getName()).log(Level.SEVERE, null, ex);
            }
            fila.enqueue(usuario.getMatricula(), usuario.getCpf());
        
            this.preencherTabela();
        }
        
    }//GEN-LAST:event_btnCadastrarActionPerformed
    
